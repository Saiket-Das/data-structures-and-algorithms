#include<iostream>
using namespace std;     // by using that we don't need to write std::cout everytime

int main () {

    // How to print in C++

    std::cout << "hello world!\n";
    std::cout << "hello " << "world!" << endl;

    // Multiple line print
    cout << "hello " << endl <<"world!" << endl;     // endl use for add new line in C++

    // Varible print in C++
    int a = 100, b = 20;
    cout << a << " " << b;


    return 0;
}